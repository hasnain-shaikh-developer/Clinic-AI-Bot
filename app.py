_MONTHS = {
    "jan":1,"feb":2,"mar":3,"apr":4,"may":5,"jun":6,
    "jul":7,"aug":8,"sep":9,"oct":10,"nov":11,"dec":12,
    "january":1,"february":2,"march":3,"april":4,"june":6,
    "july":7,"august":8,"september":9,"october":10,"november":11,"december":12,
}


def parse_user_date(text):
    """
    Flexible parser: "20 April", "April 20 2026", "2026-04-20", "20/04/2026".
    Returns a date object or None.
    """
    text  = text.strip()
    today = date.today()

    for fmt in ("%Y-%m-%d", "%d/%m/%Y", "%m/%d/%Y", "%d-%m-%Y"):
        try:
            return datetime.strptime(text, fmt).date()
        except ValueError:
            pass

    tokens = text.replace(",", " ").split()
    day = month = year = None
    for tok in tokens:
        if tok.isdigit():
            val = int(tok)
            if val > 31:
                year = val
            elif day is None:
                day = val
            else:
                year = val
        else:
            m = _MONTHS.get(tok.lower())
            if m:
                month = m

    if day and month:
        if year is None:
            year = today.year
            try:
                if date(year, month, day) < today:
                    year = today.year + 1
            except ValueError:
                pass
        try:
            return date(year, month, day)
        except ValueError:
            return None
    return None


def format_date(d):
    """date → '04 May 2026'"""
    return d.strftime("%d %B %Y")


def today_str():
    return format_date(date.today())


def example_future_date():
    """Dynamic example, never hardcoded."""
    return format_date(date.today() + timedelta(days=3))


def clinic_end_time():
    """
    Parse the closing time from clinic_timings and return a datetime.time object.
    Returns None if unparseable (so we fail open — allow same-day booking).
    """
    timings = cfg("clinic_timings")
    for sep in [" – ", " - ", "–", "—", "-"]:
        if sep in timings:
            _, raw_end = timings.split(sep, 1)
            try:
                return _parse_clinic_time(raw_end.strip())
            except ValueError:
                return None
    return None


def earliest_bookable_date():
    """
    Return the earliest date a patient can book.

    Rules:
      - Never allow past dates.
      - If today's clinic session has already ended (current time >= closing time),
        today is also blocked → return tomorrow.
      - Otherwise today is still valid → return today.
    """
    today = date.today()
    end   = clinic_end_time()

    if end is not None:
        now_time = datetime.now().time()
        if now_time >= end:
            # Clinic is closed for today — earliest slot is tomorrow
            return today + timedelta(days=1)

    return today


def validate_date(text):
    """
    Parse the user's date input and validate it.

    Rejects:
      - Unparseable input
      - Strictly past dates  (before today)
      - Today when clinic hours are already over

    Returns (date_obj, None) on success, (None, error_message) on failure.
    """
    parsed = parse_user_date(text)
    if parsed is None:
        return None, (
            f"I could not understand that date. "
            f"Please enter a date like: {example_future_date()}"
        )

    earliest = earliest_bookable_date()

    if parsed < earliest:
        if earliest == date.today():
            # Normal past-date rejection
            return None, (
                f"Please select a valid future date. "
                f"Today is {today_str()}."
            )
        else:
            # Clinic hours over for today — steer to tomorrow
            return None, (
                f"Our clinic is closed for today. "
                f"The earliest available date is {format_date(earliest)}.\n"
                f"Please enter {format_date(earliest)} or a later date."
            )

    return parsed, None


