#
#  SET UP in Meta Developer Console:
#    App → WhatsApp → Configuration → Webhook
#    Callback URL : https://your-domain.com/whatsapp
#    Verify Token : value of META_VERIFY_TOKEN env var
#    Subscribe to : messages
#
#  GET  /whatsapp  — Meta verification handshake (one-time setup)
#  POST /whatsapp  — receives incoming patient messages
#
#  Reply format (sent back via Meta Cloud API):
#    "Welcome to <Clinic Name> 👋
#     📅 Book Appointment:
#     <booking link>
#     💬 You can also ask your questions here"
# ══════════════════════════════════════════════════════════════════

@app.route("/whatsapp", methods=["GET"])
def whatsapp_verify():
    """
    Meta webhook verification handshake.
    Meta sends a GET with hub.challenge — we echo it back if the
    verify token matches META_VERIFY_TOKEN.
    """
    mode      = request.args.get("hub.mode")
    token     = request.args.get("hub.verify_token")
    challenge = request.args.get("hub.challenge")

    if mode == "subscribe" and token == META_VERIFY_TOKEN:
        print("[WhatsApp] Webhook verified by Meta.")
        return challenge, 200
    return "Verification failed", 403


@app.route("/whatsapp", methods=["POST"])
def whatsapp_webhook():
    """
    Receive incoming WhatsApp messages from patients via Meta API.
    Reply with a welcome message containing the booking link.

    The clinic is identified by ?clinic=<id> appended to the webhook
    URL (configure per clinic in the Meta dashboard).
    """
    data = request.get_json(silent=True) or {}

    # Extract the sender's phone number from the Meta payload
    try:
        entry   = data["entry"][0]
        change  = entry["changes"][0]
        value   = change["value"]
        message = value["messages"][0]
        sender  = message["from"]                   # patient's phone number
    except (KeyError, IndexError):
        # Not a message event (e.g. status update) — acknowledge and ignore
        return jsonify({"status": "ok"}), 200

    # Resolve clinic from ?clinic= query param
    clinic_id  = request.args.get("clinic", "")
    clinic_row = CLINICS.get(clinic_id)
    base       = get_base_url()

    if clinic_row:
        clinic_name  = clinic_row["name"]
        booking_link = f"{base}/?clinic={clinic_id}"
    else:
        clinic_name  = cfg("clinic_name")
        booking_link = f"{base}/"

    reply_text = (
        f"Welcome to {clinic_name} 👋\n\n"
        f"📅 Book Appointment:\n"
        f"{booking_link}\n\n"
        f"💬 You can also ask your questions here"
    )

    # Send the reply via Meta Cloud API
    if META_TOKEN and META_PHONE_ID:
        try:
            api_url = f"https://graph.facebook.com/v19.0/{META_PHONE_ID}/messages"
            payload = json.dumps({
                "messaging_product": "whatsapp",
                "to":                sender,
                "type":              "text",
                "text":              {"body": reply_text},
            }).encode("utf-8")

            req = urllib.request.Request(
                api_url,
                data    = payload,
                method  = "POST",
                headers = {
                    "Authorization": f"Bearer {META_TOKEN}",
                    "Content-Type":  "application/json",
                },
            )
            urllib.request.urlopen(req, timeout=10)
            print(f"[WhatsApp] Welcome reply sent to {sender}")
        except Exception as exc:
            print(f"[WhatsApp] Reply error: {exc}")
    else:
        print(f"[WhatsApp] Meta creds not set — would reply to {sender}: {reply_text}")

    # Always return 200 to Meta to acknowledge receipt
    return jsonify({"status": "ok"}), 200


# ══════════════════════════════════════════════════════════════════
#  PER-CLINIC ADMIN DASHBOARDS
#  Each clinic in CLINICS gets its own route: /admin-<id>
#  These are generated automatically — no manual route needed when
#  you add a new clinic to the CLINICS dict.
#
#  Each dashboard shows only that clinic's appointments (filtered
#  by clinic_id stored on each appointment at booking time).
# ══════════════════════════════════════════════════════════════════

def _make_clinic_admin(cid):
    """
    Factory that returns a view function for a given clinic_id.
    We use a factory so the closure captures cid correctly in a loop.
    """
    def clinic_admin_view():
        period      = request.args.get("filter", "all")
        clinic_info = CLINICS[cid]

        # Load only this clinic's appointments
        all_records = [
            a for a in load_appointments()
            if a.get("clinic_id") == cid
        ]
        filtered_records = filter_appointments(all_records, period)

        slot_summary = {}
        for appt in all_records:
            d = appt.get("date", "Unknown")
            slot_summary[d] = slot_summary.get(d, 0) + 1

        today_count = len(filter_appointments(all_records, "today"))

        # Build a minimal CLINIC_CONFIG-compatible dict for the template
        clinic_ctx = {
            **CLINIC_CONFIG,                          # keep colours/timings/fees
            "clinic_name":  clinic_info["name"],
            "admin_path":   clinic_info["dashboard"].lstrip("/"),
        }

        return render_template(
            "admin.html",
            appointments  = filtered_records,
            all_count     = len(all_records),
            today_count   = today_count,
            clinic        = clinic_ctx,
            slot_summary  = slot_summary,
            active_filter = period,
        )

    # Flask requires unique endpoint names
    clinic_admin_view.__name__ = f"clinic_admin_{cid}"
    return clinic_admin_view


# Register one route per clinic  (e.g. /admin-abc, /admin-xyz)
for _cid, _clinic_data in CLINICS.items():
    _path = _clinic_data["dashboard"]          # e.g. "/admin-abc"
    app.add_url_rule(_path, endpoint=f"clinic_admin_{_cid}",
                     view_func=_make_clinic_admin(_cid))


